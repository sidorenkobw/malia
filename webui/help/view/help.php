        <h1>Help</h1>
        <p>Step 1: In your Chrome Browser go to https://maliaspeech.org/. </p>
        <p>Step 2: Log in to the website with your google account.</p>
        <p>Step 3: Go to the learn page.</p>
        <p>Step 4: Edit the text. You can either type into the consul what you want to read into the program or you can copy and paste a passage that you want to read into the consul.</p>
        <p>Step 5: If you are ready to begin recording immediately then you can click “Start Recording.”  If you want the website to save your edits in order to record later then click “Finish Editing.”
</p>
        <p>Step 6: Speak into a microphone that it connected to the computer that you are using and make sure you see cat heads after “Incoming sound meter:” (Anywhere from 0 to 5, but they should constantly be fluctuating.)</p>
        <p>Step 8: Say only the word that is boxed in red and then click “Next Word.”</p>
        <p>Step 9: Once the next word has the red box around it, you may say it without clicking anything else and it will record the word. Please take note of the fact that the “Words uploaded:” count has in fact changed. 
</p>
        <p>Step 10: If you want to skip a word and not record it, the “Next Word” key will move the red box to the next word without recording the previous word. Please take note of the fact that the “Words uploaded:” count has in fact not changed even though you have passed another word. </p>
        <p>Step 11: When you get to the last word of the passage or you feel like finishing your recording session click “Stop Recording. 
</p>
        <p>Troubleshooting:
</p>
        <p>If the cats are not appearing on the screen, fully shut down the Chrome internet browser and restart it.</p>
        <p>If the recording times out on you while you are using it, please note that between every word you are given 10 seconds to say the word otherwise you will be timed out. If this is a continuous issue you are running into please get into contact with the builders of the website to get this changed. 
</p>
       

