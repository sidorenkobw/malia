        <h1>About</h1>

        <p>Malia’s Speech is an open source project that is attempting to enable people who are hard of speech to be
            understood more easily by people around them. This project was inspired by an eleven year old girl with
            cerebral palsy who wanted to find a way for the world to better understand what she had to say. So her
            mother Donna Sanchez had the idea to have a microphone and speech system that would be able to clarify
            the words that Malia says.</p>
        <p>The project was first worked on at the <a herf="http://tomglobal.org/makeathons/2017/3/17/tomberkeley-2017">
                2017 UC Berkeley Enable Tech Makeathon.</a></p>
        <p>The parent organization to this event is <a herf="http://tomglobal.org"> TIKKUN OLAM MAKERS by the Reut Group.</a></p>
        <p>The founding team to work on Maila’s Speech was made up of five professionals Jordan Aronson,
            Dinesh Belwalker, Drew Perttulla, David Stav, and Andrew Sidorenko, and three UC Berkeley Students
            Andy Kang, Aditya Palacharla, and Joshua Toch.</p>
